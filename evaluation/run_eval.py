"""
Automated evaluation harness for DubaiPulse Analyst.

Runs the benchmark questions through the real orchestrator (using whatever
LLM_PROVIDER is configured — Ollama by default) and scores each on the rubric
from eval_questions.md, then writes a markdown + JSON report.

Run from the repo root (needs the LLM provider available, e.g. `ollama serve`):

    python evaluation/run_eval.py
"""

from __future__ import annotations

import asyncio
import json
import os
import sys
from pathlib import Path

# Make the backend importable and run from there (so data paths resolve).
REPO_ROOT = Path(__file__).resolve().parents[1]
BACKEND = REPO_ROOT / "backend"
sys.path.insert(0, str(BACKEND))
os.chdir(BACKEND)

from app.agents.orchestrator import Orchestrator  # noqa: E402
from app.tools.llm import build_llm  # noqa: E402

# (question, expected_route)
BENCHMARK = [
    ("Which zones saw the biggest price increase in 2024?", "analytical"),
    ("Compare rental yields across Downtown Dubai and Dubai Marina in 2025.", "analytical"),
    ("Why did off-plan transaction volume change in early 2022?", "analytical"),
    ("What is the average price per sqft in Palm Jumeirah for secondary sales in 2025?", "simple"),
    ("How did the CBUAE base rate relate to secondary price growth from 2022 to 2024?", "analytical"),
    ("Which communities offer the highest rental yield in 2025?", "analytical"),
    ("Compare off-plan vs secondary price per sqft in Business Bay over 2023-2025.", "analytical"),
    ("What was the average apartment price per sqft in Dubai Marina in 2025?", "simple"),
]


def score(question: str, expected_route: str, final: dict) -> dict:
    ver = final.get("verification", {})
    cit = final.get("citations", {})
    grounded = bool(ver.get("verified")) and cit.get("row_count", 0) > 0
    routed = final.get("route") == expected_route
    cited = cit.get("row_count", 0) > 0
    conf_flagged = final.get("confidence") in ("high", "medium", "low")  # always set
    passed = grounded and cited
    return {
        "question": question,
        "expected_route": expected_route,
        "route": final.get("route"),
        "confidence": final.get("confidence"),
        "verified": f"{ver.get('verified_count', 0)}/{ver.get('numbers_checked', 0)}",
        "rows": cit.get("row_count", 0),
        "grounded": grounded,
        "routed_correctly": routed,
        "cited": cited,
        "confidence_flagged": conf_flagged,
        "passed": passed,
        "answer": (final.get("answer") or "")[:160],
    }


async def main() -> None:
    orch = Orchestrator(llm=build_llm())
    results = []
    for i, (q, route) in enumerate(BENCHMARK, 1):
        print(f"[{i}/{len(BENCHMARK)}] {q}")
        try:
            final = await orch.run(q)  # no stream needed for eval
        except Exception as exc:  # noqa: BLE001
            print(f"    ERROR: {exc}")
            final = {"verification": {}, "citations": {}, "confidence": "low", "route": None, "answer": ""}
        row = score(q, route, final)
        results.append(row)
        print(f"    route={row['route']} conf={row['confidence']} verified={row['verified']} "
              f"grounded={row['grounded']} passed={row['passed']}")

    # --- Aggregate ---
    n = len(results)
    passed = sum(r["passed"] for r in results)
    grounded = sum(r["grounded"] for r in results)
    routed = sum(r["routed_correctly"] for r in results)
    summary = {
        "total": n,
        "passed": passed,
        "grounded": grounded,
        "routed_correctly": routed,
        "pass_rate": round(passed / n * 100, 1),
        "grounded_rate": round(grounded / n * 100, 1),
        "routing_accuracy": round(routed / n * 100, 1),
    }

    out_dir = REPO_ROOT / "evaluation"
    (out_dir / "eval_report.json").write_text(
        json.dumps({"summary": summary, "results": results}, indent=2)
    )

    # --- Markdown report ---
    lines = [
        "# DubaiPulse Analyst — Evaluation Report",
        "",
        "_Generated by `python evaluation/run_eval.py` against the real orchestrator._",
        "",
        "## Summary",
        "",
        f"- **Questions:** {n}",
        f"- **Passed (grounded + cited):** {passed}/{n}  ({summary['pass_rate']}%)",
        f"- **Grounded (verifier-confirmed):** {grounded}/{n}  ({summary['grounded_rate']}%)",
        f"- **Routing accuracy:** {routed}/{n}  ({summary['routing_accuracy']}%)",
        "",
        "## Per-question results",
        "",
        "| # | Question | Route (exp.) | Confidence | Verified | Rows | Pass |",
        "|---|----------|--------------|------------|----------|------|------|",
    ]
    for i, r in enumerate(results, 1):
        route_cell = f"{r['route']} ({r['expected_route']})"
        pass_cell = "✅" if r["passed"] else "⚠️"
        lines.append(
            f"| {i} | {r['question']} | {route_cell} | {r['confidence']} | "
            f"{r['verified']} | {r['rows']} | {pass_cell} |"
        )
    (out_dir / "eval_report.md").write_text("\n".join(lines) + "\n")

    print("\n" + "=" * 60)
    print(f"PASS {passed}/{n} ({summary['pass_rate']}%) · "
          f"grounded {grounded}/{n} · routing {routed}/{n}")
    print(f"Report written to evaluation/eval_report.md")


if __name__ == "__main__":
    asyncio.run(main())
